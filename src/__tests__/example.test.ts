/**
 * Example Test
 *
 * This test suite contains a single test case to demonstrate a basic test using Jest.
 * This test case checks whether the addition of 1 and 1 results in the expected value of 2.
 */
it('should pass', () => {
  // The `expect` function is used to define the expected behavior or outcome of a test.
  // The `toBe` matcher checks if the actual value matches the expected value (2 in this case).
  expect(1 + 1).toBe(2);
});
